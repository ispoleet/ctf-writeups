# Build a dictionary between the last byte of the stream cipher (intermediate) and the plaintext
# byte. On every round we increment plaintext by 1. This means that the last byte of the plaintext
# is always NULL, so we can work backwards. For example:
#
#   31: f(58, B5) = 18 ^ 00 = 18  -->  PRNGs[0x18] = 0xB5
#
# Given 0x18 we can recover 0xB5 and therefore we can re-generate the stream cipher, so we can
# decrypt one round. 
def gen_stream_ciphers():
    global PRNGs

    for byte in range(256):       
        stream = gen_xor_byte(prng_stream[-1], byte)

        PRNGs[stream] = byte   


