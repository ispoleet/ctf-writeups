# -------------------------------------------------------------------------------------------------
# Generates a XOR-byte
def gen_xor_byte(x, y):
    z = 0
    
    while x > 0:
        if x & 1 == 1:
            z ^= y;
        
        x >>= 1
        y <<= 1

        if y >= 0x100:
            y ^= 0x11D

    return z & 0xff


